def hours_to_minutes(hours):
    return hours * 60


def hours_to_seconds(hours):
    return hours * 3600


def minutes_to_seconds(minutes):
    return minutes * 60


def minutes_to_hours(minutes):
    return minutes / 60


def seconds_to_minutes(seconds):
    return seconds / 60


def seconds_to_hours(seconds):
    return seconds / 3600

